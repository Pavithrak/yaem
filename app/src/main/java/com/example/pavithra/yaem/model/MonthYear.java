package com.example.pavithra.yaem.model;

public class MonthYear {
    private Integer year;
    private Integer month;

    public MonthYear(Integer year, Integer month) {
        this.year = year;
        this.month = month;
    }

    public Integer getYear() {
        return year;
    }

    public Integer getMonth() {
        return month;
    }
}
